package com.convertbinarie.ConvertBinarie;

public class ConvertRequest {
    
    private String value;
    private String valueIn;
    private String convertIn;

    // Getters e setters
    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getValueIn() {
        return valueIn;
    }

    public void setValueIn(String valueIn) {
        this.valueIn = valueIn;
    }

    public String getConvertIn() {
        return convertIn;
    }

    public void setConvertIn(String convertIn) {
        this.convertIn = convertIn;
    }
}
